									<p>Цена определяется целями, регионом раскрутки и конкуренцией в отрасли. Бюджет определяется самостоятельно. Важной характеристикой рекламы в Google AdWords является стоимость клика. Неправильно считать: чем ниже показатель, тем эффективнее проходит рекламная кампания. Высокая стоимость – высокая позиция объявления в блоке, а значит, и большее количество привлеченных посетителей (трафика).</p>
									<p>Однако неразумно учитывать лишь стоимость контекстной рекламы в Google. Ее важность нивелируется при проведении кампаний, предполагающих оплату за результат (цель).</p>
									<p>Формула для расчета:Цк = Рк / Пкр + $0,01</p>
									<p>Рк (рейтинг конкурента) получается путем умножения его ставки на показатель качества (Пкр). Допустим, первый рекламодатель указывает $12, а Пкр  – 6, рейтинг будет равняться 72. Второй конкурент имеет более низкую ставку – $8, но Пкр – 8. Его рейтинг составит 64.</p>
									<p>На основании этих показателей можно рассчитать стоимость клика: 64/6 + 0,01 = $10,67. Приблизительно такая сумма будет к списанию, если пользователь кликнет.</p>
									<p>С ростом конкуренции увеличивается цена клика. Чтобы платить меньше, можно подобрать не такие конкурентные запросы. Однако в этом случае нужно подготовиться к тому, что объем трафика будет меньше.</p>
									<p>Для проверки конкуренции следует ввести запрос в строку поиска. Если выдача насыщена рекламными предложениями, стоимость клика будет высокой. Фактическую цена определяется «Планировщиком ключевых слов». В «Прежних показателях» можно определить конкурентность запроса и ориентировочные ставки рекламодателей.</p>
