                                <p>Прототипирование — первый шаг разработчика, продумывающего функционал ресурса. В его ходе будут определены структурные основы, выбран наиболее подходящий способ размещения блоков, проанализирована эффективность будущего сайта. Принимается во внимание колоссальное количество факторов, в том числе предпочтения целевой аудитории. Фактически прототип — это не черновой вариант, а первая модель базового функционала, определяющая его возможности и проверяющая их доступность.</p>
                                <p>Ни в коем случае не стоит путать прототип с дизайном — это разные термины, пусть и являющиеся смежными. Всякая фирма, которая занимается разработкой сайтов, имеет в своем составе специалиста по проектировке. В наших реалиях это очень востребованная профессия: без подобного мастера разработка фактически обречена на провал.</p>
                                <p>Закономерно, что и наша компания имеет в своем штате подобного специалиста: мы идём в ногу со временем, учитывая текущие тенденции. Оттого и проекты получают положительные отзывы клиентов: при разработке учитываются все нюансы. Значительно облегчается и работа дизайнера, который занимается непосредственно своей сферой деятельности, не беря на себя потребность принятия относящихся к другой сфере решений.</p>
