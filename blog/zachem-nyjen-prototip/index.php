                                <h3>Юзабилити и предпочтения ЦА</h3>
                                <p>Для того, чтобы создать хорошую модель, необходимо принимать во внимание предпочтения целевой аудитории. Здесь подспорьем станут статистические данные, позволяющие составить определенное представление. Необходимо проанализировать модель поведения «среднего» пользователя: куда он будет заходить, на что упадет его взгляд, сколько времени он будет изучать информацию.</p>
                                <p>Дизайн имеет огромное значение — этот факт неоспорим, — но он относится скорее к сфере эстетики, чем практики. Создание функционала обуславливает именно удобство использования ресурса. Например, пользователь должен найти в привычном месте информацию о контактах.</p>
                                <p>Можно провести простейшую параллель: дизайн — это финальная отделка, другими словами — нечто внешнее. Архитектура сайта — прототип — скорее соотносится с фундаментом, расположением стен, проведенными коммуникационными линиями. Это основа, на которую впоследствии будет ложиться оформление.</p>
                                <h3>Наши преимущества</h3>
                                <p>Избегая чрезмерной скромности, выделим преимущества конкретно нашей компании в виде перечня:</p>
                                <ul>
                                    <li>Ориентация на клиента. Мы понимаем клиента, учитываем его мнение во всех вопросах. Функционал, визуальная картинка, общая структура — все решения по разработке принимаются лишь после подробного обсуждения с заказчиком.</li>
                                    <li>Отчетность. Мы неизменно уведомляем клиента на каждом из этапов, чтобы вы точно представляли себя ход работ и достигнутые результаты.</li>
                                    <li>Результативность. Мы нацелены в первую очередь на успешный результат, дотошно изучили все существующие правила и тенденции. Вы можете быть уверены в том, что вверяете работу профессионалам — и говорим об этом мы без ложной патетики. </li>
                                    <li>Временные затраты. Работы осуществляются в минимизированные сроки — столь быстро, сколь это возможно без ущерба результату. Переделывать что-либо впоследствии едва ли будет нужно: подходящий клиенту сайт обычно выходит сходу. При необходимости «косметические работы» могут быть проведены и на последнем этапе.</li>
                                </ul>
